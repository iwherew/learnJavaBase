package com.mif;

public class IfDemo {
    public static void main(String[] args) {
        int num = 10;
        if (num < 0) {
            System.out.println('1');
        } else if (num < 50) {
            System.out.println('2');
        } else {
            System.out.println('3');
        }
    }
}
